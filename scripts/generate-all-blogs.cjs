// Generate all blog posts
function generateAllBlogs() {
  const tools = toolBlogData.ko;
  let generated = 0;
  let skipped = 0;

  for (const [slug, data] of Object.entries(tools)) {
    const outputDir = path.join(process.cwd(), 'src', 'content', 'blog', 'ko');
    const outputPath = path.join(outputDir, `${slug}-guide.md`);

    // Skip if already exists (like severance already created)
    if (fs.existsSync(outputPath)) {
      console.log(`⏭️  Skipped (exists): ${slug}`);
      skipped++;
      continue;
    }

    const publishDate = new Date().toISOString().split('T')[0];

    const frontmatter = `---
title: "${data.title}"
description: "${data.description}"
publishDate: "${publishDate}"
category: "${data.category}"
emoji: "${data.emoji}"
relatedTool: "/ko/tools/${slug}"
relatedPosts: []
keywords:
${data.keywords.map(k => `  - ${k}`).join('\n')}
faq:
${data.faq.map(item => `  - question: "${item.q}"
    answer: "${item.a}"`).join('\n')}
---`;

    const markdown = `${frontmatter}
${data.content}
`;

    // Ensure directory exists
    if (!fs.existsSync(outputDir)) {
      fs.mkdirSync(outputDir, { recursive: true });
    }

    fs.writeFileSync(outputPath, markdown, 'utf-8');
    console.log(`✅ Generated: ${slug}-guide.md`);
    generated++;
  }

  console.log(`\n📊 Summary: ${generated} generated, ${skipped} skipped`);
}

generateAllBlogs();
